namespace NeoUI.Blazor;

/// <summary>
/// Defines the layout alignment for Timeline items.
/// </summary>
public enum TimelineAlign
{
    /// <summary>Icon is centered with date on the left and content on the right (default).</summary>
    Center,
    /// <summary>Icon and content are left-aligned.</summary>
    Left,
    /// <summary>Icon and content are right-aligned.</summary>
    Right,
    /// <summary>Items alternate between left and right.</summary>
    Alternate
}
