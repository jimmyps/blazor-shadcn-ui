namespace NeoUI.Blazor;

/// <summary>
/// Defines how the connector line fits between timeline icons.
/// </summary>
public enum TimelineConnectorFit
{
    /// <summary>Connector has a small ring gap between the icon and the line (default).</summary>
    Spaced,
    /// <summary>Connector line connects directly to each icon with no gap.</summary>
    Connected
}
