namespace BlazorUI.Components.Typography;

/// <summary>
/// Defines the typography variant styles.
/// </summary>
public enum TypographyVariant
{
    /// <summary>
    /// Heading level 1 - largest heading.
    /// </summary>
    H1,

    /// <summary>
    /// Heading level 2.
    /// </summary>
    H2,

    /// <summary>
    /// Heading level 3.
    /// </summary>
    H3,

    /// <summary>
    /// Heading level 4.
    /// </summary>
    H4,

    /// <summary>
    /// Paragraph text - default body text.
    /// </summary>
    P,

    /// <summary>
    /// Blockquote - quoted text.
    /// </summary>
    Blockquote,

    /// <summary>
    /// Inline code text.
    /// </summary>
    InlineCode,

    /// <summary>
    /// Lead paragraph - larger emphasized text.
    /// </summary>
    Lead,

    /// <summary>
    /// Large text.
    /// </summary>
    Large,

    /// <summary>
    /// Small text.
    /// </summary>
    Small,

    /// <summary>
    /// Muted text - de-emphasized.
    /// </summary>
    Muted
}
