namespace NeoUI.Blazor;

/// <summary>Defines the column layout of a dynamic form.</summary>
public enum FormLayout
{
    /// <summary>One column (all fields full-width, default).</summary>
    SingleColumn = 1,
    /// <summary>Two column grid layout.</summary>
    TwoColumn = 2,
    /// <summary>Three column grid layout.</summary>
    ThreeColumn = 3,
    /// <summary>Four column grid layout.</summary>
    FourColumn = 4
}
