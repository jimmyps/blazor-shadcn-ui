namespace BlazorUI.Components.DataTable;

/// <summary>
/// Specifies the text alignment for a DataTable column.
/// </summary>
public enum ColumnAlignment
{
    /// <summary>
    /// Left-aligned text (default).
    /// </summary>
    Left,

    /// <summary>
    /// Center-aligned text.
    /// </summary>
    Center,

    /// <summary>
    /// Right-aligned text (typically used for numeric columns).
    /// </summary>
    Right
}
