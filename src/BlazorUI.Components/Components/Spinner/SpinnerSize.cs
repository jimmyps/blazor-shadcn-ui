{
    /// <summary>
    /// Small spinner (16x16 pixels).
    /// </summary>
    Small,

    /// <summary>
    /// Medium spinner (32x32 pixels).
    /// Default size.
    /// </summary>
    Medium,

    /// <summary>
    /// Large spinner (48x48 pixels).
    /// </summary>
    Large
}
